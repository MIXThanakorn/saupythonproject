# for

for x in range(5): #range คือ จำนวนครั้งโดยเริ่มจาก 0
    print(f"DTI {x}")

for y in range(2,5): #เลขตัวแรกคือจุดเริ่มต้น
    print(f"SAU {y}")

print(".....................")

for z in range(2,5,3): #เลขตัวที่ 3 คือ step คือจำนวนการทำงาน
    print(f"WOW {z}")

for a in range(3,30,5):
    print("HI...")

print("hello")
